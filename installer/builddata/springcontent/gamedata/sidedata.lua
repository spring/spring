local TDF = VFS.Include('gamedata/parse_tdf.lua')

--------------------------------------------------------------------------------
--------------------------------------------------------------------------------

if (not VFS.FileExists('gamedata/sidedata.tdf')) then
  return false
end

local sidedata, err = TDF.Parse('gamedata/sidedata.tdf')
if (sidedata == nil) then
  error('Error parsing sidedata.tdf: ' .. err)
end

--------------------------------------------------------------------------------

return sidedata

